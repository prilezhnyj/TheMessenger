import UIKit

class SetupFont {
    static func sfProBold(sizeFont: CGFloat) -> UIFont {
        return UIFont(name: "SFPro-Bold", size: sizeFont)!
    }
    
    static func sfProRegular(sizeFont: CGFloat) -> UIFont {
        return UIFont(name: "SFPro-Regular", size: sizeFont)!
    }
}
