import UIKit

class SetupFont {
    static func avenirHeavy(sizeFont: CGFloat) -> UIFont {
        return UIFont(name: "Avenir-Heavy", size: sizeFont)!
    }
    
    static func avenirRoman(sizeFont: CGFloat) -> UIFont {
        return UIFont(name: "Avenir-Roman", size: sizeFont)!
    }
}
