import UIKit

class SetupColor {
    static func purpleColor() -> UIColor {
        return #colorLiteral(red: 0.5924316049, green: 0.4650813341, blue: 0.8050958514, alpha: 1)
    }
    
    static func orangeColor() -> UIColor {
        return #colorLiteral(red: 1, green: 0.5882352941, blue: 0.4431372549, alpha: 1)
    }
    
    static func whiteColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    
}
